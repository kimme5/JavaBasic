package tutorial4;

public class Main02 {
	
	public static void main(String[] args) {
		
		int a = 10;
		System.out.println("현재의 a는 " + a + "입니다.");		// 10
		a++;
		System.out.println("현재의 a는 " + a + "입니다."); 		// 11
		System.out.println("현재의 a는 " + ++a + "입니다.");	// 12
		System.out.println("현재의 a는 " + a++ + "입니다.");	// 12
		System.out.println("현재의 a는 " + a + "입니다.");		// 13

		System.out.println("현재의 a는 " + --a + "입니다.");	// 12
		System.out.println("현재의 a는 " + a-- + "입니다.");	// 12
		System.out.println("현재의 a는 " + a + "입니다.");		// 11
		
	}

}
